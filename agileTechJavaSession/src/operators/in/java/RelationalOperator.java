package operators.in.java;

public class RelationalOperator {

	public static void main(String[] args) {
		// Relational operator  <,>,<=,>=,==,!=      --  returns boolean -- true/false
		
		int a = 20;
		int b = 20;
		
		System.out.println(a<b); // false
		System.out.println(a>b); // false
		System.out.println(a<=b); // true
		System.out.println(a>=b); // true
		System.out.println(a==b);  // true
		System.out.println(a!=b);  // false
		}

}
